public class Shop {
    private String walls;
    private String furniture;
    private String shoeRacks;
    private String clothesRacks;

    public void setWalls(String walls) {
        this.walls = walls;
    }

    public void setFurniture(String furniture) {
        this.furniture = furniture;
    }

    public void setShoeRacks(String shoeRacks) {
        this.shoeRacks = shoeRacks;
    }

    public void setClothesRacks(String clothesRacks) {
        this.clothesRacks = clothesRacks;
    }
}